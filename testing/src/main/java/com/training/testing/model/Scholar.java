package com.training.testing.model;

public class Scholar {
	private String inum;
	private String name;
	private Integer joinYear;
	private Integer courseDur;
	
	//Defining the getters and setters...
	public String getInum() {
		return inum;
	}
	public void setInum(String inum) {
		this.inum = inum;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getJoinYear() {
		return joinYear;
	}
	public void setJoinYear(Integer joinYear) {
		this.joinYear = joinYear;
	}
	public Integer getCourseDur() {
		return courseDur;
	}
	public void setCourseDur(Integer courseDur) {
		this.courseDur = courseDur;
	}
	

}
